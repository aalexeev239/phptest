<h2>Немного поэзии</h2>
<p>Я не рожден царей забавить<br>
Пора, пора! рога трубят<br>
Тебя восторгами поздравить<br>
<br>
Как он умел забыть себя<br>
На вист вечерний приезжает<br>
Любите самого себя<br>
<br>
Здесь каждый шаг в душе рождает<br>
Теперь то холмик, то ручей<br>
Душа кипит и замирает<br>
<br>
Когда узнала меж гостей<br>
В деревне нашей видеть вас<br>
Мельканьем платьев и речей.</p>